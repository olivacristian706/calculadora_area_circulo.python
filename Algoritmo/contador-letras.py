# --------------------------------------------------------------------
# Algoritmo para contar la cantidad de letras en una palabra o frase
# --------------------------------------------------------------------

# Paso 1: solicitar entrada al usuario

palabra_ingresada = input ("Por favor, ingresa una palabra o frase: ")
    
# Paso2: contar la cantidad de letras

cantidad_letras = len(palabra_ingresada)
    
# Paso3: mostrar al usuario el resultado

print("La palabra o frase tiene", cantidad_letras, "letras.")
    